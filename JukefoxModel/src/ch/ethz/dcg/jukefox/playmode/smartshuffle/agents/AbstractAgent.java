package ch.ethz.dcg.jukefox.playmode.smartshuffle.agents;


/**
 * Abstract agent that provides some basic functionality for the child classes.
 */
public abstract class AbstractAgent implements IAgent {

	@Override
	public String getIdentifier() {
		switch (getAgentType()) {
			case Random:
				return "random";
			case Repetition:
				return "repetition";
			case Suggested:
				return "suggested";
			case Top:
				return "top";
			default:
				assert false;
				return "";
		}
	}

}
