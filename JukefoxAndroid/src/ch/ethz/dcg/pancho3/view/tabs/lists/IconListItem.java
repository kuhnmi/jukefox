package ch.ethz.dcg.pancho3.view.tabs.lists;


public class IconListItem {
	
	public static final int TYPE_ALBUM = 0;
	
	private String line1;
	private String line2;
	private String iconPath;
	private int id;
	private int type;
	
	

	public IconListItem(String line1, String line2, String path, int id, int type) {
		this.line1 = line1;
		this.line2 = line2;
		this.iconPath = path;
		this.id = id;
		this.type = type;
	}
	
	public IconListItem(String line1, int id, int type) {
		this.line1 = line1;
		this.line2 = "";
		this.iconPath = "";
		this.id = id;
		this.type = type;
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	
	public String getLine1() {
		return line1;
	}
	public void setLine1(String line1) {
		this.line1 = line1;
	}
	public String getLine2() {
		return line2;
	}
	public void setLine2(String line2) {
		this.line2 = line2;
	}
	public String getIconPath() {
		return iconPath;
	}
	public void setIconPath(String iconPath) {
		this.iconPath = iconPath;
	}
	
	public int getType() {
		return type;
	}
	

}
